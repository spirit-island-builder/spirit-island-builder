  <h1 id="about" class="subtitle is-3">About</h1>
  <p>
    The Spirit Island Builder is a fan-made and unofficial tool for creating custom Spirit Island
    content. It is a passion project for me, Resonant, with contributions and support from a number
    of different individuals to whom I&#39;m incredibly grateful. The Builder was built as an
    interface to an older tool called &quot;the Template&quot;, to which I also contributed before
    envisioning it as a website version.
  </p>
  <p>
    My goal with this project is to lower the barrier to entry for aspiring homebrew Spirit Island
    game designers. I hope the Builder makes custom content creation faster, more exciting, and more
    rewarding.
  </p>
